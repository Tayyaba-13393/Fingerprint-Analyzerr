import socket
import threading
import time
import os

import requests as http_requests
from scapy.all import sniff, wrpcap, IP
 
CAPTURE_DIR     = "captures"
CAPTURE_TIMEOUT = 10
PCAP_FILENAME   = "capture.pcap"
PCAP_A          = "capture_a.pcap"
PCAP_B          = "capture_b.pcap"
 
os.makedirs(CAPTURE_DIR, exist_ok=True)
 
